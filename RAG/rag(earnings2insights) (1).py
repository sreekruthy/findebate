def ingest_subset(base_path, doc_type):
   
    if not os.path.exists(base_path):
        print(f" Path not found: {base_path}")
        return 0

    folders = [
        f for f in os.listdir(base_path)
        if not f.startswith('.')
        and os.path.isdir(os.path.join(base_path, f))
    ]

    total_chunks = 0
    skipped      = 0

    for folder in folders:
        source_path = os.path.join(base_path, folder, "source", "source.md")

        if not os.path.exists(source_path):
            print(f" No source.md in {folder}, skipping")
            skipped += 1
            continue

        with open(source_path, "r", encoding="utf-8") as f:
            text = f.read()

        chunks = chunk_text(text)

        if not chunks:
            print(f" No chunks from {folder}, using full text as fallback")
            chunks = [text[:3000]]

        embeddings = model.encode(
            chunks,
            batch_size=64,
            convert_to_numpy=True,
            show_progress_bar=False
        ).tolist()

        ids = [
            f"{doc_type}_{folder}_chunk_{j}"
            for j in range(len(chunks))
        ]
        metadatas = [
            {
                "source_file" : folder,
                "type"        : doc_type,
                "chunk_index" : j
            }
            for j in range(len(chunks))
        ]

        try:
            existing_ids = set(collection.get(ids=ids)["ids"])
        except Exception:
            existing_ids = set()

        new_mask = [i for i, cid in enumerate(ids) if cid not in existing_ids]

        if new_mask:
            collection.add(
                documents  = [chunks[i]     for i in new_mask],
                embeddings = [embeddings[i] for i in new_mask],
                ids        = [ids[i]        for i in new_mask],
                metadatas  = [metadatas[i]  for i in new_mask]
            )
            total_chunks += len(new_mask)

    print(f" {doc_type:>15} | {len(folders)-skipped:>3} docs "
          f"| {total_chunks:>5} new chunks indexed "
          f"| {skipped} skipped")
    return total_chunks

