def retrieve_by_dimension(dimension, doc_type_filter=None, top_k=5):
  
    if dimension not in DIMENSION_QUERIES:
        raise ValueError(f"Unknown dimension '{dimension}'. "
                         f"Choose from: {list(DIMENSION_QUERIES.keys())}")

    seen   = set()
    merged = []

    for query in DIMENSION_QUERIES[dimension]:
        results = retrieve(query, top_k=top_k, doc_type_filter=doc_type_filter)
        for r in results:
            uid = f"{r['source_file']}_chunk_{r['chunk_id']}"
            if uid not in seen:
                seen.add(uid)
                merged.append(r)

    merged.sort(key=lambda x: x["score"], reverse=True)
    return merged[:top_k]


def retrieve_all_dimensions(doc_type_filter=None, top_k=3):
  
    return {
        dim: retrieve_by_dimension(dim, doc_type_filter=doc_type_filter, top_k=top_k)
        for dim in DIMENSION_QUERIES
    }

def get_agent_context(source_file=None, top_k=3):
    fetch_k = top_k * 10 if source_file else top_k

    dims = {}
    for dim in DIMENSION_QUERIES:
        results = retrieve_by_dimension(dim, top_k=fetch_k)
        if source_file:
            results = [r for r in results
                      if r["source_file"] == source_file][:top_k]
        dims[dim] = results

    return {
        "earnings_agent"  : dims["general_financial"] + dims["specialized_metrics"],
        "market_agent"    : dims["general_financial"] + dims["multi_query_integration"],
        "sentiment_agent" : dims["market_sentiment_risk"],
        "valuation_agent" : dims["specialized_metrics"] + dims["general_financial"],
        "risk_agent"      : dims["market_sentiment_risk"] + dims["specialized_metrics"]
    }

